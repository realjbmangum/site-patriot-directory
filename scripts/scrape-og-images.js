#!/usr/bin/env node

/**
 * scrape-og-images.js — Fetch og:image for each business listing
 *
 * Visits each business website, extracts the og:image (or twitter:image)
 * meta tag, and outputs UPDATE SQL statements.
 *
 * Usage:
 *   node scripts/scrape-og-images.js
 *
 * Output: data/photos-update.sql
 *
 * Then apply:
 *   npx wrangler d1 execute patriot_directory_db --remote --file=data/photos-update.sql
 */

import { writeFileSync } from 'fs';

const OUTPUT_FILE = 'data/photos-update.sql';
const DELAY_MS = 800;

const BUSINESSES = [
  { slug: 'jimmys-famous-seafood',              url: 'https://jimmysfamousseafood.com' },
  { slug: 'ex-umbris-designs',                  url: 'https://exumbrisdesigns.com' },
  { slug: 'toor-knives',                        url: 'https://toorknives.com' },
  { slug: 'from-logo-to-leather',               url: null }, // X-only, no website
  { slug: 'chagrin-valley-soap-salve',          url: 'https://www.chagrinvalleysoapandsalve.com' },
  { slug: 'little-seed-farm',                   url: 'https://littleseedfarm.com' },
  { slug: 'debra-bruner-studio',                url: 'https://www.debrabrunerstudio.com' },
  { slug: 'enak-flavors',                       url: 'https://enakflavors.com' },
  { slug: 'infiniti-crafting-company',          url: 'https://www.infiniticraftingcompany.com' },
  { slug: 'the-doghouse-coffee',                url: 'https://thedoghouse.coffee' },
  { slug: 'original-appearance-manufacturing',  url: 'https://oamusa.com' },
  { slug: 'kickin-back-kreations',              url: 'https://kickinbackkreations.com' },
  { slug: 'happy-life-ranch',                   url: 'https://happyliferanch.com' },
];

function sleep(ms) {
  return new Promise((r) => setTimeout(r, ms));
}

function extractOgImage(html) {
  // Try og:image first, then twitter:image, then first large <img>
  const patterns = [
    /<meta[^>]+property=["']og:image["'][^>]+content=["']([^"']+)["']/i,
    /<meta[^>]+content=["']([^"']+)["'][^>]+property=["']og:image["']/i,
    /<meta[^>]+name=["']twitter:image["'][^>]+content=["']([^"']+)["']/i,
    /<meta[^>]+content=["']([^"']+)["'][^>]+name=["']twitter:image["']/i,
  ];

  for (const re of patterns) {
    const m = html.match(re);
    if (m && m[1] && m[1].startsWith('http')) return m[1].trim();
  }
  return null;
}

function resolveUrl(imageUrl, siteUrl) {
  if (!imageUrl) return null;
  if (imageUrl.startsWith('http')) return imageUrl;
  try {
    return new URL(imageUrl, siteUrl).href;
  } catch {
    return null;
  }
}

async function fetchOgImage(url) {
  try {
    const res = await fetch(url, {
      headers: {
        'User-Agent':
          'Mozilla/5.0 (compatible; PatriotDirectoryBot/1.0; +https://patriotbusiness.directory)',
        Accept: 'text/html,application/xhtml+xml',
      },
      redirect: 'follow',
      signal: AbortSignal.timeout(12000),
    });

    if (!res.ok) {
      console.log(`  ✗ HTTP ${res.status}`);
      return null;
    }

    const html = await res.text();
    const raw = extractOgImage(html);
    return resolveUrl(raw, url);
  } catch (err) {
    console.log(`  ✗ ${err.message}`);
    return null;
  }
}

async function main() {
  const updates = [];
  const skipped = [];

  for (const biz of BUSINESSES) {
    if (!biz.url) {
      console.log(`⏭  ${biz.slug} — no website, skipping`);
      skipped.push(biz.slug);
      continue;
    }

    process.stdout.write(`🔍 ${biz.slug} ... `);
    const photoUrl = await fetchOgImage(biz.url);

    if (photoUrl) {
      console.log(`✓ ${photoUrl.slice(0, 80)}`);
      const escaped = photoUrl.replace(/'/g, "''");
      updates.push(
        `UPDATE businesses SET photo_url = '${escaped}' WHERE slug = '${biz.slug}';`
      );
    } else {
      console.log(`✗ no og:image found`);
      skipped.push(biz.slug);
    }

    await sleep(DELAY_MS);
  }

  const sql = [
    '-- Auto-generated by scripts/scrape-og-images.js',
    `-- Run: npx wrangler d1 execute patriot_directory_db --remote --file=${OUTPUT_FILE}`,
    '',
    ...updates,
  ].join('\n');

  writeFileSync(OUTPUT_FILE, sql);

  console.log('\n─────────────────────────────────────────');
  console.log(`✓ ${updates.length} photo URLs found`);
  console.log(`✗ ${skipped.length} skipped: ${skipped.join(', ')}`);
  console.log(`\nSQL written to ${OUTPUT_FILE}`);
  console.log(
    `Apply with: npx wrangler d1 execute patriot_directory_db --remote --file=${OUTPUT_FILE}`
  );
}

main();
